package util;

/**
 * Enumeration of every direction in a 2D world in a determined landmark.
 */
public enum Direction {

    /**
     * Up direction.
     */
    UP,
    /**
     * Down direction.
     */
    DOWN,
    /**
     * Left direction.
     */
    LEFT,
    /**
     * Right direction.
     */
    RIGHT,
    /**
     * It represents the state in which there is no direction.
     */
    NEUTRAL;
}
